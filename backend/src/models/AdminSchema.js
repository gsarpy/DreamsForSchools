let mongoose = require('mongoose');

let AdminSchema = new mongoose.Schema({
  email: {
    type: String,
    unique: true,
    required: true,
    trim: true
  },
  username: {
    type: String,
    unique: true,
    required: true,
    trim: true
  },
  password: {
    type: String,
    required: true,
  },
  passwordConf: {
    type: String,
    required: true,
  }
});

let AdminSchema = mongoose.model('Admin', AdminSchema);
module.exports = AdminSchema;
